# simple calculator only '+', '-', '*', '/'
first = int(input('Type a fist number: '))
second = int(input('Type a second number: '))

# '+'
sum = first + second
print(first, '+', second, '=', sum)

# '-'
subtraction = first - second
print(first, '-', second, '=', subtraction)

# '*'
multiply = first * second
print(first, '*', second, '=', multiply)

# '/'
division = first / second
print(first, '/', second, '=', division)
